    //Fonction Modifier (Etablit la connexion a la base)
    public void Modifier() throws SQLException{
        //Declaration des variables
        int unId = 0, unStock = 0;
        String unLib;
        float unPrix = 0;
        Produit leProd;
        
        //Recuperation du formulaire 
        unLib = jTextField2.getText();
        try {
            unId = Integer.parseInt(jTextField1.getText());
            unPrix = Integer.parseInt(jTextField3.getText());
            unStock = Integer.parseInt(jTextField4.getText());
        } catch(NumberFormatException nfe) {
            // pas un entier
        }
        
        
        //Appel a la base de donnees 
        int modifProd = new Bdd().modif("UPDATE produit SET lib = '"+unLib+"', pu = "+unPrix+", qte = "+unStock+" WHERE id = "+unId+";");
        
        //On affiche le resultat (avec erreur)
        if (modifProd==1){
            jLabel7.setForeground(Color.green);
            jLabel7.setText("Le produit a bien était enregistré.");
        } else if (modifProd==0) {
            jLabel7.setForeground(Color.red);
            jLabel7.setText("Une Erreur s'est produite. \nMerci de réesayer");
        }
        
        
        jDialog1.setSize(300, 100);;
        jDialog1.setVisible(true);
    }
        
    
    
    
    
    
