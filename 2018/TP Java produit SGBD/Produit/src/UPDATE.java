    // Fonction ajouter produit
    public void AjouterProduit(){
        int unId = 0, unStock = 0, testReussite;
        String unLibelle;
        float unPrix = 0;
        Produit leProd;
        
        unLibelle = jTextField2.getText();
        try {
            unId = Integer.parseInt(jTextField1.getText());
            unPrix = Integer.parseInt(jTextField3.getText());
            unStock = Integer.parseInt(jTextField4.getText());
        } catch(NumberFormatException nfe) {
            // pas un entier
            jDialog1.setBackground(java.awt.Color.white);
            jLabel7.setText("Erreur !! \nIl fallait un INT");
        }
        leProd = new Produit(unId, unLibelle, unPrix, unStock);
        try {
            PasserelleBdd.modifProd('A', leProd);
            testReussite = 1;
        } catch (SQLException ex) {
            Logger.getLogger(UPDATE.class.getName()).log(Level.SEVERE, null, ex);
            testReussite = 2;
        }
        
        // On verifie si le produit existe deja dans la base
        int testId = 9090;
        int testIdStock = 9090;
        float testIdPrix = 9090;
        try {
            testId = PasserelleBdd.rechercherProd(unId).getId();
            testIdStock = PasserelleBdd.rechercherProd(unId).getStock();
            testIdPrix = PasserelleBdd.rechercherProd(unId).getPrix();            
        } catch (SQLException ex) {
            Logger.getLogger(UPDATE.class.getName()).log(Level.SEVERE, null, ex);
        }
        /**
         * !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
         * !!MANQUE LA GESTION D'ERREUR LORSQUE LE PRODUIT A BIEN ETAIT ENREGISTRE!!
         * !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
         */
        if (testId==unId && testIdStock==0 && testIdPrix==0){
            testReussite = 3;
        }
        // On affiche le resultat (echec ou reussite)
        if (testReussite==1){
            jLabel7.setForeground(Color.green);
            jLabel7.setText("Le produit a bien était enregistré.");
        } else if (testReussite==2) {
            jLabel7.setForeground(Color.red);
            jLabel7.setText("Une Erreur s'est produite. \nMerci de réesayer");
        } else if (testReussite==3) {
            jLabel7.setForeground(Color.red);
            jLabel7.setText("L'identifiant existe deja !\nMerci de réesayer");
        }
        
        jDialog1.setSize(300, 100);;
        jDialog1.setVisible(true);
    }
    
    
    
